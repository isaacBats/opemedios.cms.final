<h2>Quiénes somos</h2>
<p>Alfonso Marina inicia su actividad como ebanista en 1971 teniendo como meta ofrecer un producto de la más alta calidad.</p>
<p>Seleccionar y diseñar productos es una actividad muy personal; en este proceso, el contexto cultural, la experiencia profesional y un gusto particular influyen en la elección de un proyecto definido. Ser consistente con este proceso da una personalidad y un carácter que conecta a todas las piezas de la colección.</p>
<p>El objetivo de Alfonso Marina es ofrecer piezas superiores con una elegancia casual, aquellas que muestran armonía en el diseño y la proporción, sin limitarse a cierto periodo histórico o procedencia.</p>
<p>Los acabados de los productos de Alfonso Marina requieren la labor de artesanos calificados para lograr el sabor auténtico del aviejado y la pátina. Hoy en día conserva vigentes los procesos tradicionales en las piezas pintadas, con elementos decorativos que enfatizan el carácter de la colección.</p>
<p>Los herrajes ornamentales que complementan muchos de los productos se elaboran en un taller artesanal propio, en el cual se forjan piezas de distintos metales, incluido el hierro, logrando herrajes auténticos con libertad en el diseño.</p>
<p>Alfonso Marina exporta un alto porcentaje de sus productos a través de showrooms dirigidos fundamentalmente a diseñadores y detallistas de prestigio. En México cuenta con una impresionante sala de exhibición de 1,800 m2 que ofrece una amplia gama de productos de ambientación, la cual incluye textiles, iluminación, accesorios y arte.</p>
